package com.tm.designpatterns.oop;

public class Base3 {
    /*
     * 静态字段 静态方法
     *
     * static修饰
     *
     * 静态字段不属于实例
     *
     * 使用类名.静态字段访问
     * （不推荐用实例变量.静态字段去访问静态字段，
     *   因为在Java程序中，实例对象并没有静态字段。
     *   在代码中，实例对象能访问静态字段只是因为编译器可以根据实例类型自动转换为类名.静态字段来访问静态对象。）
     *
     * 静态方法也经常用于辅助方法。
     * */
}
